    def check_and_finish_match(self, bot_id: str, bot_data: Dict) -> bool:
        """üèÅ –ü—Ä–æ–≤–µ—Ä—è–µ—Ç –∏ –∑–∞–≤–µ—Ä—à–∞–µ—Ç –º–∞—Ç—á –±–æ—Ç–∞ –µ—Å–ª–∏ –æ–Ω –¥–ª–∏—Ç—Å—è —Å–ª–∏—à–∫–æ–º –¥–æ–ª–≥–æ"""
        try:
            user_id = bot_data["id"]
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –∞–∫—Ç–∏–≤–Ω—ã–π –º–∞—Ç—á
            response = self.session.get(
                f"{self.base_url}/api-game-match/user/{user_id}",
                timeout=10
            )
            
            if response.status_code == 200:
                matches_data = response.json()
                
                if matches_data and len(matches_data) > 0:
                    match = matches_data[0]  # –ü–µ—Ä–≤—ã–π –∞–∫—Ç–∏–≤–Ω—ã–π –º–∞—Ç—á
                    match_id = match.get("matchId")
                    
                    if match_id and match.get("status") == "InProgress":
                        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –∫–æ–≥–¥–∞ –º–∞—Ç—á –Ω–∞—á–∞–ª—Å—è
                        match_start_time = bot_data.get("match_start_time")
                        if match_start_time:
                            start_time = datetime.fromisoformat(match_start_time)
                            now = datetime.now()
                            match_duration = (now - start_time).total_seconds()
                            
                            # –ï—Å–ª–∏ –º–∞—Ç—á –∏–¥–µ—Ç –±–æ–ª—å—à–µ 30-90 —Å–µ–∫—É–Ω–¥, –∑–∞–≤–µ—Ä—à–∞–µ–º –µ–≥–æ
                            if match_duration > random.randint(30, 90):
                                return self.finish_match(bot_id, bot_data, match_id, match)
                        else:
                            # –ï—Å–ª–∏ –Ω–µ—Ç –≤—Ä–µ–º–µ–Ω–∏ –Ω–∞—á–∞–ª–∞, —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –µ–≥–æ
                            with self.lock:
                                self.bots_data[bot_id]["match_start_time"] = datetime.now().isoformat()
                                
                return True
            else:
                return False
                
        except Exception as e:
            logging.error(f"‚ùå –ò—Å–∫–ª—é—á–µ–Ω–∏–µ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ –º–∞—Ç—á–∞ –±–æ—Ç–∞ {bot_id}: {e}")
            return False

    def finish_match(self, bot_id: str, bot_data: Dict, match_id: int, match_data: Dict) -> bool:
        """üèÅ –ó–∞–≤–µ—Ä—à–∞–µ—Ç –º–∞—Ç—á –±–æ—Ç–∞ —Å–æ —Å–ª—É—á–∞–π–Ω—ã–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º"""
        try:
            players_list = match_data.get("playersId", "[]")
            if isinstance(players_list, str):
                import json
                players_list = json.loads(players_list)
            
            if not players_list:
                logging.warning(f"‚ö†Ô∏è –ù–µ—Ç –∏–≥—Ä–æ–∫–æ–≤ –≤ –º–∞—Ç—á–µ {match_id}")
                return False
                
            # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω–æ–≥–æ –ø–æ–±–µ–¥–∏—Ç–µ–ª—è
            winner_id = random.choice(players_list)
            losers = [p for p in players_list if p != winner_id]
            
            finish_data = {
                "Winners": [winner_id],
                "Losers": losers
            }
            
            response = self.session.post(
                f"{self.base_url}/api-game-match/{match_id}/finish",
                json=finish_data,
                timeout=10
            )
            
            if response.status_code == 200:
                logging.info(f"üèÜ –ë–æ—Ç {bot_data['username']} –∑–∞–≤–µ—Ä—à–∏–ª –º–∞—Ç—á {match_id}. –ü–æ–±–µ–¥–∏—Ç–µ–ª—å: {winner_id}")
                
                # –û—á–∏—â–∞–µ–º –¥–∞–Ω–Ω—ã–µ –æ –º–∞—Ç—á–µ
                with self.lock:
                    self.bots_data[bot_id]["match_found"] = False
                    self.bots_data[bot_id]["match_id"] = None
                    self.bots_data[bot_id]["match_start_time"] = None
                    
                return True
            else:
                logging.error(f"‚ùå –û—à–∏–±–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è –º–∞—Ç—á–∞ {match_id} –¥–ª—è –±–æ—Ç–∞ {bot_data['username']}: {response.status_code}")
                return False
                
        except Exception as e:
            logging.error(f"‚ùå –ò—Å–∫–ª—é—á–µ–Ω–∏–µ –ø—Ä–∏ –∑–∞–≤–µ—Ä—à–µ–Ω–∏–∏ –º–∞—Ç—á–∞ –±–æ—Ç–∞ {bot_id}: {e}")
            return False

    def simulate_bot_behavior(self, bot_id: str, bot_data: Dict) -> bool:
        """üé≠ –°–∏–º—É–ª–∏—Ä—É–µ—Ç –ø–æ–≤–µ–¥–µ–Ω–∏–µ –±–æ—Ç–∞"""
        try:
            behavior = BotBehavior(bot_data.get("behavior", "random"))
            settings = self.behavior_settings[behavior]
            bot_name = bot_data.get("username", "Unknown")
            
            # –°–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∏—Ä—É–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ –æ—á–µ—Ä–µ–¥–∏ –∫–∞–∂–¥—ã–µ 10 —Ü–∏–∫–ª–æ–≤
            if random.random() < 0.1:  # 10% —à–∞–Ω—Å —Å–∏–Ω—Ö—Ä–æ–Ω–∏–∑–∞—Ü–∏–∏
                self.sync_queue_status(bot_id, bot_data)
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ –≤ –º–∞—Ç—á–µ –ª–∏ –±–æ—Ç
            if bot_data.get("current_match_id"):
                return False
            
            # –ï—Å–ª–∏ –±–æ—Ç –≤ –æ—á–µ—Ä–µ–¥–∏, –ø—Ä–æ–≤–µ—Ä—è–µ–º timeout
            if bot_data.get("in_queue", False):
                queue_join_time = bot_data.get("queue_join_time")
                if queue_join_time:
                    join_time = datetime.fromisoformat(queue_join_time)
                    queue_duration = settings["queue_duration"]
                    max_wait = random.randint(*queue_duration)
                    
                    # –û—á–µ–Ω—å –º–∞–ª–µ–Ω—å–∫–∞—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –ø–æ–∫–∏–Ω—É—Ç—å –æ—á–µ—Ä–µ–¥—å (—Ç–æ–ª—å–∫–æ 3% —à–∞–Ω—Å)
                    if ((datetime.now() - join_time).total_seconds() > max_wait and 
                        random.random() < 0.03):
                        logging.info(f"üö™ –ë–æ—Ç {bot_name} –ø–æ–∫–∏–¥–∞–µ—Ç –æ—á–µ—Ä–µ–¥—å (timeout –ø–æ—Å–ª–µ {max_wait} —Å–µ–∫)")
                        return self.leave_queue(bot_id, bot_data)
                
                # –î–∞–∂–µ –µ—Å–ª–∏ –≤ –æ—á–µ—Ä–µ–¥–∏, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º True –∫–∞–∫ –¥–µ–π—Å—Ç–≤–∏–µ
                return True
            
            # –†–µ—à–∞–µ–º, –≤–æ–π—Ç–∏ –ª–∏ –≤ –æ—á–µ—Ä–µ–¥—å - –ë–û–õ–ï–ï –ê–ì–†–ï–°–°–ò–í–ù–û
            queue_chance = random.random()
            if queue_chance < settings["queue_probability"]:
                match_type = random.choice(settings["match_types"])
                logging.info(f"üéØ –ë–æ—Ç {bot_name} ({behavior.value}) –ø—ã—Ç–∞–µ—Ç—Å—è –≤–æ–π—Ç–∏ –≤ –æ—á–µ—Ä–µ–¥—å {match_type.name} (chance: {queue_chance:.2f})")
                return self.join_queue(bot_id, bot_data, match_type)
            
            # –î–∞–∂–µ –µ—Å–ª–∏ –Ω–µ –≤—Ö–æ–¥–∏–º –≤ –æ—á–µ—Ä–µ–¥—å, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º True –∫–∞–∫ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç—å
            return True
            
        except Exception as e:
            logging.error(f"‚ùå –ò—Å–∫–ª—é—á–µ–Ω–∏–µ –ø—Ä–∏ —Å–∏–º—É–ª—è—Ü–∏–∏ –ø–æ–≤–µ–¥–µ–Ω–∏—è –±–æ—Ç–∞ {bot_id}: {e}")
            return False

    def create_bots_batch(self, start_index: int, count: int) -> List[Dict]:
        """üè≠ –°–æ–∑–¥–∞–µ—Ç –≥—Ä—É–ø–ø—É –±–æ—Ç–æ–≤ –ø–∞—Ä–∞–ª–ª–µ–ª—å–Ω–æ"""
        results = []
        
        with ThreadPoolExecutor(max_workers=10) as executor:
            futures = []
            
            for i in range(start_index, start_index + count):
                bot_id = f"bot_{i:03d}"
                future = executor.submit(self.register_bot, bot_id)
                futures.append((bot_id, future))
            
            for bot_id, future in futures:
                try:
                    result = future.result(timeout=60)
                    if result:
                        results.append(result)
                except Exception as e:
                    logging.error(f"‚ùå –û—à–∏–±–∫–∞ —Å–æ–∑–¥–∞–Ω–∏—è –±–æ—Ç–∞ {bot_id}: {e}")
        
        return results

